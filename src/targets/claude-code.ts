/**
 * Claude Code adapter — `@anthropic-ai/claude-code`.
 *
 * The richest target: skills, subagents, hooks, permissions and output styles
 * are all native. The one quirk that matters: Claude Code reads `CLAUDE.md`, NOT
 * `AGENTS.md`, and has no fallback — so we emit `AGENTS.md` (the canonical text)
 * plus a generated `CLAUDE.md` that imports it via `@AGENTS.md` (Windows-safe;
 * we never rely on a symlink for this).
 */
import type { Adapter, FileOutput, ProjectionResult } from './types.js';
import type { HarnessSpec } from '../config/canonical.js';
import {
  rootAgentsOutput,
  nestedAgentsOutputs,
  skillOutputs,
  markdownDefOutputs,
  permRuleString,
} from './shared.js';
import { compileClaudeEnforcement } from '../enforce/hooks.js';
import { toClaudeMcp, projectScoped, jsonFile } from '../mcp/transpile.js';

const CLAUDE_MD = `@AGENTS.md

<!-- Generated by meta-harness-studio. Edit .harness/AGENTS.md (projected to AGENTS.md),
     not this file. Claude-only additions can go below; they are not projected to other tools. -->
`;

function project(spec: HarnessSpec): ProjectionResult {
  const files: FileOutput[] = [];
  const warnings: string[] = [];

  // Instructions: canonical AGENTS.md + a CLAUDE.md import shim.
  const root = rootAgentsOutput(spec, 'claude-code');
  if (root) files.push(root);
  files.push(...nestedAgentsOutputs(spec));
  files.push({
    path: 'CLAUDE.md',
    contents: CLAUDE_MD,
    capability: 'instructions',
    scope: 'project',
  });

  // Skills, subagents, commands, output styles — all native markdown surfaces.
  files.push(...skillOutputs(spec, '.claude/skills'));
  files.push(...markdownDefOutputs(spec.agents, '.claude/agents', 'subagents'));
  files.push(...markdownDefOutputs(spec.commands, '.claude/commands', 'commands'));
  files.push(...markdownDefOutputs(spec.outputStyles, '.claude/output-styles', 'outputStyles'));

  // MCP — project-scoped servers go in .mcp.json.
  const projectServers = projectScoped(spec.mcp);
  if (projectServers.length) {
    files.push({
      path: '.mcp.json',
      contents: jsonFile(toClaudeMcp(projectServers)),
      capability: 'mcp',
      scope: 'project',
    });
  }
  const nonProject = spec.mcp.filter((s) => s.scope !== 'project');
  if (nonProject.length) {
    warnings.push(
      `${nonProject.length} MCP server(s) are scope=user/local — place them in ~/.claude.json by hand: ${nonProject
        .map((s) => s.name)
        .join(', ')}.`,
    );
  }

  // Permissions + enforcement -> .claude/settings.json (the deterministic layer).
  const enforcement = compileClaudeEnforcement(spec.enforce);
  warnings.push(...enforcement.warnings);

  const allow = spec.permissions.allow.map(permRuleString);
  // permissions.toml denies and enforce.toml deny invariants can name the same
  // rule (e.g. a Read deny + a block-env-read enforce rule) — dedupe them.
  const deny = [...new Set([...spec.permissions.deny.map(permRuleString), ...enforcement.deny])];
  const ask = spec.permissions.ask.map(permRuleString);

  const settings: Record<string, unknown> = {};
  if (allow.length || deny.length || ask.length) {
    settings.permissions = {
      ...(allow.length ? { allow } : {}),
      ...(deny.length ? { deny } : {}),
      ...(ask.length ? { ask } : {}),
    };
  }
  if (Object.keys(enforcement.hooks).length) {
    settings.hooks = enforcement.hooks;
  }
  if (Object.keys(settings).length) {
    files.push({
      path: '.claude/settings.json',
      contents: jsonFile(settings),
      capability: 'permissions',
      scope: 'project',
    });
  }

  return { files, warnings };
}

export const claudeCode: Adapter = {
  id: 'claude-code',
  title: 'Claude Code',
  homepage: 'https://code.claude.com',
  capabilities: {
    instructions: 'shim',
    commands: 'native',
    subagents: 'native',
    hooks: 'native',
    mcp: 'native',
    permissions: 'native',
    modes: 'native',
    outputStyles: 'native',
    skills: 'native',
    ignore: 'shim',
  },
  project,
};
